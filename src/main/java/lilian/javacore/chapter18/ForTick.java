package main.java.lilian.javacore.chapter18;
// Демонстрация применения опретаора цикла for
public class ForTick {
    public static void main(String[] args) {


        for (int n = 10; n > 0; n--)
            System.out.println("такт " + n);
        for (int n=10; n>0; n--)
            System.out.println("такт " + n);
    }
}
